# Faça um programa que verifique se a pessoa pertence à família “calvo”.

print("Vamos descobrir se você pertence a familía 'Calvo' ou 'Silva'?")

nome = input("Digite aqui o seu sobrenome? ")

if nome == 'Calvo' :
  print("Você pertence a familia 'Calvo'!")

elif nome == 'Silva' :
  print("Você pertence a familia 'Silva'! Que demais!!")

else :
  print("Ah que pena, mas não deixa de ser um sobrenome legal!")